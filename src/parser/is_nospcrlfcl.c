int					is_nospcrlfcl(unsigned char c)
{
	return ((c >= 0x01 && c <= 0x09) ||
			(c >= 0x0b && c <= 0x0c) ||
			(c >= 0x0e && c <= 0x1f) ||
			(c >= 0x21 && c <= 0x39) ||
			(c >= 0x3b));
}
